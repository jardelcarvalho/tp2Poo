    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        lerCampos();
        if(CPF.length() != 14){
            JOptionPane.showMessageDialog(null, "CPF inválido!");
            return;
        }
        String[] parametro = {nome, CPF};
        if(FactoryProducer.getFactory("controller").getController("cliente").gravarObjeto(parametro)){
            JOptionPane.showMessageDialog(null, "Cliente cadastrado!");
        }else{
            JOptionPane.showMessageDialog(null, "Falha ao cadastrar cliente!");
        }
        limparCampos();
        preencherTabela();
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        lerCampos();
        if(CPF2.length() != 14){
            JOptionPane.showMessageDialog(null, "CPF inválido!");
            return;
        }
        if(FactoryProducer.getFactory("controller").getController("cliente").apagarObjeto(CPF2)){
            JOptionPane.showMessageDialog(null, "Cliente apagado!");
        }else{
            JOptionPane.showMessageDialog(null, "Falha ao apagar cliente!");
        }
        limparCampos();
        preencherTabela();
        
    }//GEN-LAST:event_jButton2ActionPerformed


