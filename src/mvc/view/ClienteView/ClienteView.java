    @Override
    public void preencherTabela(){
        itensTabela = FactoryProducer.getFactory("controller").getController("cliente").buscarObjetos();
        if(itensTabela == null) return;
        DefaultTableModel tableModel = new DefaultTableModel(1, 2);
        for(String[] dados : itensTabela){
            tableModel.addRow(new Object[]{dados[0], dados[1]});
        }
        jTable1.setModel(tableModel);
    }
    
